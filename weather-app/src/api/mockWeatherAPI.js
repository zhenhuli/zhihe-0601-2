const weekDays = ['周日', '周一', '周二', '周三', '周四', '周五', '周六'];

const randomInt = (min, max) => Math.floor(Math.random() * (max - min + 1)) + min;

const randomCondition = () => conditions[randomInt(0, conditions.length - 1)];

const getDateLabel = (daysFromNow) => {
  if (daysFromNow === 0) return '今天';
  if (daysFromNow === 1) return '明天';
  if (daysFromNow === 2) return '后天';
  const date = new Date();
  date.setDate(date.getDate() + daysFromNow);
  return weekDays[date.getDay()];
};

const generateWeatherData = (cityName) => {
  const baseTemp = randomInt(15, 35);
  const condition = randomCondition();
  
  const current = {
    temp: baseTemp + randomInt(-3, 3),
    condition: condition.name,
    icon: condition.icon,
    humidity: randomInt(30, 95),
    windSpeed: randomInt(1, 30),
    feelsLike: baseTemp + randomInt(-2, 5)
  };

  const forecast = [];
  let prevHigh = baseTemp + 5;
  let prevLow = baseTemp - 5;

  for (let i = 1; i <= 15; i++) {
    const dayCondition = randomCondition();
    const tempVariation = randomInt(-3, 3);
    const high = Math.max(prevHigh + tempVariation, -10);
    const low = Math.min(prevLow + tempVariation, high - 3);
    
    forecast.push({
      date: getDateLabel(i),
      high: Math.min(Math.max(high, -10), 45),
      low: Math.min(Math.max(low, -15), high - 3),
      condition: dayCondition.name,
      icon: dayCondition.icon
    });

    prevHigh = high;
    prevLow = low;
  }

  return {
    city: cityName,
    country: 'CN',
    current,
    forecast
  };
};

const fetchWeather = (cityName) => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      const normalizedCity = cityName.trim();
      
      if (!normalizedCity) {
        reject(new Error('请输入城市或区县名称'));
        return;
      }

      const exactMatch = cities.find(city => city === normalizedCity);
      if (exactMatch) {
        resolve(generateWeatherData(exactMatch));
        return;
      }

      reject(new Error(`未找到 "${cityName}" 的天气数据，请确认名称是否正确。\n\n提示：支持全国所有市县级行政区，如：\n市级：北京、上海、广州、深圳、成都、杭州\n县级：朝阳、海淀、浦东、双流、南山、福田`));
    }, 300);
  });
};

export default fetchWeather;
